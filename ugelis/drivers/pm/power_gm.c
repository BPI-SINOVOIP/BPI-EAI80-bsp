static void pm_rtc_callback(rtc_callback_type type)
{
    if (type == RTC_CALLBACK_ALARM)
    {
        rtc_cancel_alarm(rtc_dev);
    }
}

static void pm_rtc_wakeup_config(uint32_t *arg)
{
    struct rtc_cfg cfg;
    rtc_datetime rdt;
    rtc_dev = device_get_binding("GMRTC");
    if (!rtc_dev)
    {
        pm_gm_debug("Cann't get GM-RTC Device\n");
    }

    cfg.callback = pm_rtc_callback;
    rtc_enable(rtc_dev);
    rtc_set_config(rtc_dev, cfg);
    memset(&rdt, 0, sizeof(rtc_datetime));
    rdt.year = 2019;
    rdt.mon  = 11;
    rdt.day  = 27;
    rdt.hour = 11;
    rdt.min  = 7;
    rdt.sec  = 0;

    rtc_set_datetime(rtc_dev, &rdt);

    pm_gm_debug("Alarm after %d seconds\n", *arg);
    //    rtc_next_sec(rtc_dev, &rdt, *arg);
    rdt.sec +=  *arg;
    rtc_set_alarm(rtc_dev, &rdt);
}

