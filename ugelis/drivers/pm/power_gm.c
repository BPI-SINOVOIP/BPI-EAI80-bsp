static int SetPC_addr(uint32_t addr)
{
    uint32_t jumpAddr = 0;
    {
        /* jump to SRAM to run */
        jumpAddr = (*((volatile uint32_t *)(addr + 4)));
    }
    /* Jump to user application */
    JumpToApplication = (pFunction)jumpAddr;
    /* Initialize user application's Stack Pointer */
    __set_MSP(0x20010000);
    __set_PSP(*((volatile uint32_t *)addr));
    JumpToApplication();
    return 0;
}



static int pm_gm_soc_enter_low_power_mode(uint32_t low_power_mode)
{
    int ret = 0;

    UNUSED(low_power_mode);
    pm_gm_debug("###%s####\n", __FUNCTION__);
    if (low_power_mode == SHUTDOWN_MODE)
    {
        memcpy((uint32_t *)0x20001000, (uint32_t *)shutdown_code_run_on_sram, 1024 * 16);
        SetPC_addr(0x20001000);
    }
    else
    {
        memcpy((uint32_t *)0x20001000, (uint32_t *)standby_code_run_on_sram, 1024 * 16);
        SetPC_addr(0x20001000);
    }
    return ret;
}




